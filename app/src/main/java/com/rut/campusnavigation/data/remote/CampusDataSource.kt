    private fun buildRooms(buildingId: String, floors: Int, perFloor: Int): List<Room> {
        val rooms = mutableListOf<Room>()
        for (floor in 1..floors) {
            for (n in 1..perFloor) {
                val num = "$floor${n.toString().padStart(2, '0')}"
                val type = if (n % 7 == 0) RoomType.LABORATORY else RoomType.CLASSROOM
                rooms.add(Room("${buildingId}_$num", buildingId, num, floor = floor, type = type))
            }
        }
        rooms.add(Room("${buildingId}_CANTEEN", buildingId, "001", "Столовая", 1, RoomType.CAFETERIA))
        return rooms
    }

